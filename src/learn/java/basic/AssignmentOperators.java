package learn.java.basic;

public class AssignmentOperators {
	public static void main(String[] args) {
		int i = 10;
		System.out.println(i);
		
		i++;
		System.out.println(i); //11

		i += 2;
		System.out.println(i);//13
		
		i -= 2;
		System.out.println(i);//11
		
		i *= 2;
		System.out.println(i);//22
		
		i /= 2;
		System.out.println(i);//11
	}
}
