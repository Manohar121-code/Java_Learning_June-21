package learn.java.oops.settersAndGetters;

public class Student {
	private int rollNum;
	private String name;

	public void setRollNum(int rollNum) {
		this.rollNum = rollNum;
	}

	public int getRollNum() {
		return rollNum;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

}
